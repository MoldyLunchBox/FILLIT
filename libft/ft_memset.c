#include "libft.h"

void	*ft_memset(void *b, int t, size_t len)
{
	size_t	i;
	char	*c;

	c = (char *)b;
	i = 0;
	while (i < len)
	{
		c[i] = (char)t;
		i++;
	}
	b = (void *)c;
	return (b);
}
