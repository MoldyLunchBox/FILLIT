#include "libft.h"

char	*ft_strcat(char *s1, const char *s2)
{
	char	*d1;
	char	*d2;
	int		i;
	int		l;

	i = 0;
	d1 = (char *)s1;
	d2 = (char *)s2;
	l = ft_strlen(d1);
	while (d2[i])
	{
		d1[l] = d2[i];
		i++;
		l++;
	}
	d1[l] = '\0';
	s1 = d1;
	return (s1);
}
