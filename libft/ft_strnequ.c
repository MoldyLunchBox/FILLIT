#include "libft.h"

int	ft_strnequ(char const *s1, char const *s2, size_t n)
{
	size_t	i;

	i = 0;
	if (s1 != NULL && s2 != NULL)
	{
		if (n == 0)
			return ('1' - '0');
		while (s1[i] == s2[i] && i < n)
		{
			if (i == n - 1)
				return ('1' - '0');
			i++;
		}
	}
	return ('0' - '0');
}
