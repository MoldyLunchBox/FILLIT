#include "libft.h"

int	ft_strequ(char const *s1, char const *s2)
{
	int i;

	if (s1 != NULL && s2 != NULL)
	{
		i = 0;
		while (s1[i] == s2[i])
		{
			if (!s1[i] && !s2[i])
				return ('1' - '0');
			i++;
		}
	}
	return ('0' - '0');
}
